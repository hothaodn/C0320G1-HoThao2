public class KhaiBaoSuDungBien {
    public static void main(String[] args){
        int i = 10;
        double f = 20.5;
        boolean d = true;
        char c = 'a';
        String s = "Hà Nội";
        System.out.println("i = " + i);
        System.out.println("f = " + f );
        System.out.println("d = " + d );
        System.out.println("c = " + c);
        System.out.println("s = " + s);
    }
}
